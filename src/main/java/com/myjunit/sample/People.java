public class People {
	private String name;
	private int age;

	public People(String name, int age) {
		super();
		System.out.println("People constructor");
		this.name = name;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
